public class DiasSemana {

    public static void main(String[] args) {

        String[] dias = {
            "Lunes",
            "Martes",
            "Miercoles",
            "Jueves",
            "Viernes",
            "Sabado",
            "Domingo"
        };

        for (int indice = 0; indice < dias.length; indice++) {
            System.out.println(dias[indice]);
        }
    }
}